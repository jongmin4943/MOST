package org.most.temp;

public class StrIntClass {
	String keyword;
	int p;
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public int getP() {
		return p;
	}
	public void setP(int p) {
		this.p = p;
	}
	public StrIntClass(String keyword, int p) {
		this.keyword = keyword;
		this.p = p;
	}
	
	
}
